let stop = () => {
    let inpnumber = document.querySelector("#number").value;
    let count = inpnumber;

    let inter = setInterval(() => {
        let saw = document.querySelector("#showw");
        saw.innerHTML = count;

        if (count <= 0) {
            clearInterval(inter); // Stop the interval when count reaches zero
        } else {
            count--;
        }
    }, 1000);
}
